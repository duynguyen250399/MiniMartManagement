    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String id = this.txtStaffID.getText();
        String username = this.txtStaffUsername.getText();
        String password = this.txtStaffPassword.getText();
        String fullName = this.txtStaffFullName.getText();
        String email = this.txtStaffEmail.getText();
        String addr = this.txtStaffAddr.getText();
        String dateStr = this.txtStaffBirthdate.getText();
        SimpleDateFormat f = new SimpleDateFormat("dd/MM/yyyy");
        
        java.util.Date date = null;
        try {
            date = f.parse(dateStr);
        } catch (ParseException ex) {
            Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Date birthdate = new Date(date.getTime());
        String phone = this.txtStaffPhone.getText();
        String roleStr = (String) this.cbRole.getSelectedItem();
        System.out.println(roleStr);
        int role = (roleStr.equalsIgnoreCase("admin")) ? 1 : 0;

        UserDTO dto = new UserDTO(id, username, password, fullName, email, addr, phone, birthdate, role);

        UserDAO dao = new UserDAO();
        try {
            boolean result = dao.updateUser(dto);
            if (result) {
                JOptionPane.showMessageDialog(this, "Staff Information has been edited!");
                this.btnSave.setEnabled(false);
                this.btnEdit.setEnabled(true);

                // update table in UI
                int index = this.staffTable.getSelectedRow();
                Vector v = new Vector();
                v.add(dto.getFullName());
                v.add(dto.getEmail());
                v.add(dto.getAddr());
                v.add(dto.getPhone());
                v.add(dto.getBirthdate());
                this.staffData.set(index, v);
                this.staffs.set(index, dto);
            }
            else{
                System.out.println("update fail!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdminDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

