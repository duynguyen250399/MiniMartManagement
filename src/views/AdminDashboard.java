    private void setUpStaffTable() throws ClassNotFoundException, SQLException {
        this.staffTable.setSelectionMode(0);
        staffTableHeader = new Vector();
        staffTableHeader.add("Full Name");
        staffTableHeader.add("Email");
        staffTableHeader.add("Address");
        staffTableHeader.add("Phone");
        staffTableHeader.add("Birthdate");

        staffData = new Vector();
        UserDAO dao = new UserDAO();
        List<UserDTO> users = dao.getAllUsers();
        this.staffs = users;
        for (UserDTO user : users) {
            Vector v = new Vector();
            v.add(user.getFullName());
            v.add(user.getEmail());
            v.add(user.getAddr());
            v.add(user.getPhone());
            v.add(user.getBirthdate());
            this.staffData.add(v);
        }

        TableModel model = new DefaultTableModel(staffData, staffTableHeader) {
            @Override
            public boolean isCellEditable(int i, int i1) {
                return false;
            }

        };

        this.staffTable.setModel(model);
    }

